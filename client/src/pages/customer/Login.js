import React, { useState, useEffect } from "react";
import { Link, useNavigate, useLocation } from "react-router-dom";
import { useDispatch, useSelector } from "react-redux";
import {
  login,
  clearError,
  googleLogin,
  loadUser,
} from "../../redux/slices/authSlice";
import IMAGES from "../../config/images";
import toast from "react-hot-toast";
import { FiMail, FiLock, FiEye, FiEyeOff, FiArrowRight } from "react-icons/fi";
import { useGoogleLogin } from "@react-oauth/google";
import CompleteProfileModal from "../../components/customer/CompleteProfileModal";
import useBiometric from "../../hooks/useBiometric";

const Login = () => {
  const [formData, setFormData] = useState({
    email: "",
    password: "",
  });
  const [showPassword, setShowPassword] = useState(false);
  const [showProfileModal, setShowProfileModal] = useState(false);

  const dispatch = useDispatch();
  const navigate = useNavigate();
  const location = useLocation();

  const { user, isAuthenticated, loading, error } = useSelector(
    (state) => state.auth,
  );

  const googleClientId = process.env.REACT_APP_GOOGLE_CLIENT_ID;
  const {
    supported: biometricSupported,
    loading: biometricLoading,
    loginWithBiometric,
  } = useBiometric();

  const loginGoogle = useGoogleLogin({
    onSuccess: (tokenResponse) => {
      dispatch(googleLogin(tokenResponse.access_token))
        .unwrap()
        .then((userData) => {
          // Check if profile is incomplete
          if (
            !userData.user?.firstName ||
            !userData.user?.lastName ||
            !userData.user?.phone
          ) {
            setShowProfileModal(true);
            toast.success("Welcome! Please complete your profile.");
          } else {
            toast.success("Welcome!");
          }
        })
        .catch((err) => {
          // Error handled by slice or toast
        });
    },
    onError: () => {
      toast.error("Google login failed");
    },
  });

  const from = location.state?.from || "/";

  useEffect(() => {
    if (isAuthenticated && user) {
      // Role-based redirect
      if (user.role === "admin") {
        navigate("/admin", { replace: true });
      } else {
        navigate(from, { replace: true });
      }
    }
  }, [isAuthenticated, user, navigate, from]);

  useEffect(() => {
    if (error) {
      toast.error(error);
      dispatch(clearError());
    }
  }, [error, dispatch]);

  const handleChange = (e) => {
    setFormData({ ...formData, [e.target.name]: e.target.value });
  };

  const handleSubmit = async (e) => {
    e.preventDefault();

    if (!formData.email || !formData.password) {
      toast.error("Please fill in all fields");
      return;
    }

    try {
      await dispatch(login(formData)).unwrap();
      toast.success("Welcome!");
    } catch (err) {
      // Safely derive an error message string to avoid runtime crashes
      const errMsg =
        typeof err === "string" ? err : err?.message || JSON.stringify(err);

      // Check if it's an email verification error
      if (
        (errMsg && errMsg.includes("verify your email")) ||
        (errMsg && errMsg.includes("verification"))
      ) {
        toast.error(
          <div>
            <p>{errMsg}</p>
            <Link
              to="/resend-verification"
              className="underline text-white hover:text-gray-200"
            >
              Resend verification email
            </Link>
          </div>,
          { duration: 6000 },
        );
      }
      // Other errors handled by useEffect
    }
  };

  const handleBiometricLogin = async () => {
    if (!formData.email) {
      toast.error("Please enter your email address first");
      return;
    }
    try {
      await loginWithBiometric(formData.email);
      toast.success("Welcome!");
    } catch (err) {
      toast.error(err.message || "Biometric login failed");
    }
  };

  return (
    <div className="min-h-screen bg-gray-50 flex">
      {/* Left Side - Image */}
      <div className="hidden lg:flex lg:w-1/2 relative">
        <img
          src={IMAGES.login}
          alt="Fashion"
          className="w-full h-full object-cover"
        />
        <div className="absolute inset-0 bg-gradient-to-r from-primary-500/80 to-secondary-500/80 flex items-center justify-center">
          <div className="text-white text-center p-8">
            <h2 className="text-4xl font-bold mb-4">Welcome!</h2>
            <p className="text-xl text-white/90">
              Sign in to access your account and continue shopping
            </p>
          </div>
        </div>
      </div>

      {/* Right Side - Form */}
      <div className="w-full lg:w-1/2 flex items-center justify-center p-8">
        <div className="w-full max-w-md">
          {/* Logo */}
          <Link to="/" className="block text-center mb-8">
            <img
              src="/images/loginlogo.png"
              alt="The Fashion Gallery"
              className="h-10 w-auto object-contain mx-auto"
            />
          </Link>

          {/* Header */}
          <div className="text-center mb-8">
            <h2 className="text-2xl font-bold text-gray-800 mb-2">Sign In</h2>
            <p className="text-gray-600">
              Don't have an account?{" "}
              <Link
                to="/register"
                className="text-primary-500 hover:text-primary-600 font-medium"
              >
                Sign Up
              </Link>
            </p>
          </div>

          {/* Form */}
          <form onSubmit={handleSubmit} className="space-y-6">
            {/* Email */}
            <div>
              <label className="block text-sm font-medium text-gray-700 mb-2">
                Email Address
              </label>
              <div className="relative">
                <FiMail
                  className="absolute left-4 top-1/2 -translate-y-1/2 text-gray-400"
                  size={20}
                />
                <input
                  type="email"
                  name="email"
                  value={formData.email}
                  onChange={handleChange}
                  placeholder="Enter your email"
                  className="w-full pl-12 pr-4 py-4 border border-gray-300 rounded-xl placeholder-black focus:outline-none focus:ring-2 focus:ring-primary-500 focus:border-transparent transition-all"
                  style={{ backgroundColor: "white", color: "black" }}
                  required
                />
              </div>
            </div>

            {/* Password */}
            <div>
              <label className="block text-sm font-medium text-gray-700 mb-2">
                Password
              </label>
              <div className="relative">
                <FiLock
                  className="absolute left-4 top-1/2 -translate-y-1/2 text-gray-400"
                  size={20}
                />
                <input
                  type={showPassword ? "text" : "password"}
                  name="password"
                  value={formData.password}
                  onChange={handleChange}
                  placeholder="Enter your password"
                  className="w-full pl-12 pr-12 py-4 border border-gray-300 rounded-xl placeholder-black focus:outline-none focus:ring-2 focus:ring-primary-500 focus:border-transparent transition-all"
                  style={{ backgroundColor: "white", color: "black" }}
                  required
                />
                <button
                  type="button"
                  onClick={() => setShowPassword(!showPassword)}
                  className="absolute right-4 top-1/2 -translate-y-1/2 text-gray-400 hover:text-gray-600"
                >
                  {showPassword ? <FiEyeOff size={20} /> : <FiEye size={20} />}
                </button>
              </div>
            </div>

            {/* Remember Me & Forgot Password */}
            <div className="flex items-center justify-between">
              <label className="flex items-center gap-2 cursor-pointer">
                <input
                  type="checkbox"
                  className="w-4 h-4 text-primary-500 rounded focus:ring-primary-500"
                />
                <span className="text-sm text-gray-600">Remember me</span>
              </label>
              <Link
                to="/forgot-password"
                className="text-sm text-primary-500 hover:text-primary-600 font-medium"
              >
                Forgot Password?
              </Link>
            </div>

            {/* Submit Button */}
            <button
              type="submit"
              disabled={loading}
              className="w-full py-3 btn-gradient rounded-xl font-semibold text-base flex items-center justify-center gap-2 disabled:opacity-50"
            >
              {loading ? (
                <div className="spinner w-6 h-6 border-2 border-white border-t-transparent"></div>
              ) : (
                <>
                  Sign In
                  <FiArrowRight />
                </>
              )}
            </button>
          </form>

          {/* Divider */}
          <div className="relative my-8">
            <div className="absolute inset-0 flex items-center">
              <div className="w-full border-t border-gray-300"></div>
            </div>
            <div className="relative flex justify-center text-sm">
              <span className="px-4 bg-gray-50 text-gray-500">
                Or continue with
              </span>
            </div>
          </div>

          {/* Social Login */}
          <div className="space-y-3">
            <button
              type="button"
              onClick={() => {
                if (!googleClientId) {
                  toast.error("Google login is not configured in this build.");
                  return;
                }
                loginGoogle();
              }}
              disabled={!googleClientId}
              className={`w-full flex items-center justify-center gap-2 py-3 border border-gray-300 rounded-xl hover:bg-gray-50 transition-colors ${!googleClientId ? "opacity-60 cursor-not-allowed" : ""}`}
            >
              <svg className="w-5 h-5" viewBox="0 0 24 24">
                <path
                  fill="#4285F4"
                  d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"
                />
                <path
                  fill="#34A853"
                  d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"
                />
                <path
                  fill="#FBBC05"
                  d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"
                />
                <path
                  fill="#EA4335"
                  d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"
                />
              </svg>
              <span className="font-medium text-gray-700">Google</span>
            </button>

            {/* Biometric Login â€” only shown when device supports WebAuthn */}
            {biometricSupported && (
              <button
                type="button"
                onClick={handleBiometricLogin}
                disabled={biometricLoading}
                className="w-full flex items-center justify-center gap-2 py-3 border border-gray-300 rounded-xl hover:bg-gray-50 transition-colors disabled:opacity-50"
              >
                {biometricLoading ? (
                  <div className="spinner w-5 h-5 border-2 border-gray-400 border-t-transparent rounded-full animate-spin" />
                ) : (
                  <svg
                    className="w-5 h-5 text-gray-600"
                    viewBox="0 0 24 24"
                    fill="none"
                    stroke="currentColor"
                    strokeWidth="2"
                  >
                    <path
                      strokeLinecap="round"
                      strokeLinejoin="round"
                      d="M12 11c0-1.657-1.343-3-3-3S6 9.343 6 11v2a6 6 0 0012 0v-2c0-1.657-1.343-3-3-3s-3 1.343-3 3z"
                    />
                    <path
                      strokeLinecap="round"
                      strokeLinejoin="round"
                      d="M12 3C7.029 3 3 7.029 3 12s4.029 9 9 9 9-4.029 9-9-4.029-9-9-9z"
                    />
                  </svg>
                )}
                <span className="font-medium text-gray-700">
                  {biometricLoading ? "Verifying..." : "Biometric / Passkey"}
                </span>
              </button>
            )}
          </div>
        </div>
      </div>

      {/* Complete Profile Modal */}
      <CompleteProfileModal
        isOpen={showProfileModal}
        onClose={() => setShowProfileModal(false)}
        onComplete={(updatedUser) => {
          dispatch(loadUser());
        }}
        user={user}
      />
    </div>
  );
};

export default Login;
